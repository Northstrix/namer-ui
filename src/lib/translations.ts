export type Dictionary = typeof dictionaries['en'];

export const getDictionary = async (locale: string): Promise<Dictionary> => {
  const selectedDict = dictionaries[locale as keyof typeof dictionaries] || dictionaries.en;
  
  return new Proxy(selectedDict, {
    get: (target, prop) => {
      const key = prop as keyof Dictionary;
      if (key in target) {
        return target[key];
      }
      
      if (locale === 'es' && String(prop).startsWith('web-project-') && (String(prop).endsWith('-name'))) {
         return dictionaries.en[key];
      }

      return dictionaries.en[key] || String(key);
    },
  }) as Dictionary;
};

